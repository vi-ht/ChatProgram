     public void sendfile(){
      String path = pathFile.getText();
                            try{
                               File f;
                              f = new File(path);
                               if(!f.exists()) return;
                               FileReader fr = new FileReader(f);
                               BufferedReader br = new BufferedReader(fr); 
                               String S;
                               while((S=br.readLine()) !=null){
                                     S=S.trim();
                                     if(S.length()>0)
                                     {
                                           System.out.println(S);
                                           try {
                                                os.write("~"+S);
                                                os.newLine();
                                                os.flush(); 
                                          } catch (Exception e) {
                                                JOptionPane.showMessageDialog(this, e);
                                          }
                                     }
                               }
                               br.close(); fr.close();
                               try {
                                                os.write("~STOP");
                                                os.newLine();
                                                os.flush(); 
                                          } catch (Exception e) {
                                                JOptionPane.showMessageDialog(this, e);
                                          }
                        }
                        catch (Exception e)
                        {
                              e.getStackTrace();
                        }    
            pathFile.setText("");
            name.setText("");
            size.setText("");
}
  
